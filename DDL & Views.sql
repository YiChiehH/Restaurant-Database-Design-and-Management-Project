-- Create a function to check if the dish is available
CREATE FUNCTION dbo.IsDishAvailable(@dishId INT)
RETURNS VARCHAR(50)
AS 
BEGIN
	DECLARE @status VARCHAR(50)
    	
    SELECT @status = status FROM Dish WHERE DishId = @dishID
    
    RETURN @status;
END;

