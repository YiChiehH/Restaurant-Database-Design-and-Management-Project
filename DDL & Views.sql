CREATE FUNCTION dbo.CalculateTotalPrice (@orderId int)
RETURNS DECIMAL(10, 2)
AS
BEGIN
    DECLARE @totalPrice DECIMAL(10, 2);

    SELECT @totalPrice = SUM(tax + tips + orderValue)
    FROM dbo.[Order]
    WHERE orderId = @orderId;

    RETURN ISNULL(@totalPrice, 0);
END;


